def get_status(gffi):
    if gffi >= 75: return 'critical'
    elif gffi >= 65: return 'alert'
    elif gffi >= 58: return 'warning'
    else: return 'success'

def calculate_gffi(price_history):
    if len(price_history) < 5: return 60 + np.random.normal(0, 5)
    returns = np.diff(price_history) / price_history[:-1] * 100
    volatility = np.std(returns)
    return max(40, min(85, 50 + volatility * 2))

def fetch_country_data(country):
    try:
        ticker = yf.Ticker(country['ticker'])
        hist = ticker.history(period="1mo")
        if hist.empty: return None
        gffi = calculate_gffi(hist['Close'].values)
        return {'flag': country['flag'], 'name': country['name'], 'gffi': round(gffi, 1), 'status': get_status(gffi)}
    except:
        return None

